using System;

namespace CSharpMath
{

class Program
{
  static void Main()
  {

    Console.WriteLine(Math.Max(2,45)); //Seleciona o maior numero entre os dois
    Console.WriteLine(Math.Min(61,8)); //Seleciona o menor numero entre os dois 
    Console.WriteLine(Math.Abs(-66.2)); //Transforma o numero em positivo    
    Console.WriteLine(Math.Round(9.79)); //Arredonda o numero para o inteiro mais proximo EX: 9.79 = 10
    Console.WriteLine(Math.Sqrt(81)); //Calcula a raíz quadrada
  }   
}
}